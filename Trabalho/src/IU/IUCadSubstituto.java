    private void ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActionPerformed
        String codigo = Codigo_Text.getText();
        String nome = Nome_Text.getText();
        String titulacao = (String) Titulacao_Box.getSelectedItem();
        double salario = Double.parseDouble(Salario_Text.getText());
        String nivel = (String) Nivel_Box.getSelectedItem();
        String departamento = (String) Departamento_Box.getSelectedItem();
        int cargaHoraria = Integer.parseInt((String) Carga_Box.getSelectedItem());
        Controlador control = new Controlador();
        control.addSubstituto(codigo, nome, salario, nivel, departamento, titulacao, cargaHoraria);
        JOptionPane.showMessageDialog(this, "Docente Substituto Cadastrado!");
        Codigo_Text.setText("");
        Salario_Text.setText("");
        Nome_Text.setText("");
        Codigo_Text.requestFocus();
    }//GEN-LAST:event_ButtonActionPerformed

    private void Codigo_TextFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_Codigo_TextFocusLost
        String codigo = Codigo_Text.getText();
        Controlador control = new Controlador();
        Funcionario check = control.buscaFun(codigo);
        if(check == null){
            Nome_Text.setText("");
            Salario_Text.setText("");
            Button.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, "Funcionário existente!");
            Codigo_Text.setText(check.getCodigo());
            Nome_Text.setText(check.getNome());
            Salario_Text.setText(Double.toString(check.getSalario()));
            Button.setEnabled(false);
        }
    }//GEN-LAST:event_Codigo_TextFocusLost

