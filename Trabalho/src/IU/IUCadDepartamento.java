    private void ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActionPerformed
        String codigo = Codigo_Text.getText();
        String nome = Nome_Text.getText();
        Controlador control = new Controlador();
        control.addDepartamento(codigo, nome);
        JOptionPane.showMessageDialog(this, "Departamento Cadastrado!");
        Codigo_Text.setText("");
        Nome_Text.setText("");
        Codigo_Text.requestFocus();
    }//GEN-LAST:event_ButtonActionPerformed

    private void Codigo_TextFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_Codigo_TextFocusLost
        String codigo = Codigo_Text.getText();
        Controlador control = new Controlador();
        Departamento check = control.buscaDep(codigo);
        if(check == null){
            Nome_Text.setText("");
            Button.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, "Departamento existente!");
            Codigo_Text.setText(check.getCodigo());
            Nome_Text.setText(check.getNome());
            Button.setEnabled(false);
        }
    }//GEN-LAST:event_Codigo_TextFocusLost

